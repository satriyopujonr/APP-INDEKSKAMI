							<h5>Proses Penilaian Kelengkapan dan Kematangan Tata Kelola Keamanan Informasi</h5>
							<p style="text-align: justify;">Proses penilaian kemudian dilakukan melalui 2 (dua) metode:</p>
<p style="text-align: justify;">A.Jumlah (kelengkapan) bentuk pengamanan;</p>
<p style="text-align: justify; padding-left: 30px;">Metode pertama akan mengevaluasi sejauh mana instansi responden sudah menerapkan pengamanan sesuai dengan kelengkapan kontrol yang diminta oleh standar ISO/IEC 27001:2009.</p>
<p style="text-align: justify; padding-left: 30px;">Untuk kelima area evaluasi, yang dimaksud sebagai kontrol dijelaskan secara singkat di bawah ini:&nbsp;</p>
<p style="text-align: justify; padding-left: 60px;">►&nbsp; &nbsp;Tata Kelola Keamanan Informasi</p>
<p style="text-align: justify; padding-left: 90px;">Kontrol yang diperlukan adalah kebijakan formal yang mendefinisikan peran, tanggung-jawab, kewenangan pengelolaan keamanan informasi, dari pimpinan unit kerja sampai ke pelaksana operasional. Termasuk dalam area ini juga adalah adanya program kerja yang berkesinambungan, alokasi anggaran, evaluasi program dan strategi peningkatan kinerja tata kelola keamanan informasi.</p>
<p style="text-align: justify; padding-left: 90px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 60px;">►&nbsp; &nbsp;Pengelolaan Risiko Keamanan Informasi</p>
<p style="text-align: justify; padding-left: 90px;">Bentuk tata kelola yang diperlukan adalah adanya kerangka kerja pengelolaan risiko dengan definisi yang eksplisit terkait ambang batas diterimanya risiko, program pengelolaan risiko dan langkah mitigasi yang secara reguler dikaji efektifitasnya.</p>
<p style="text-align: justify; padding-left: 90px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 60px;">►&nbsp; &nbsp;Kerangka Kerja Keamanan Informasi</p>
<p style="text-align: justify; padding-left: 90px;">Kelengkapan kontrol di area ini memerlukan sejumlah kebijakan dan prosedur kerja operasional, termasuk strategi penerapan, pengukuran efektifitas kontrol dan langkah perbaikan.&nbsp;</p>
<p style="text-align: justify; padding-left: 90px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 60px;">►&nbsp; &nbsp;Pengelolaan Aset Informasi</p>
<p style="text-align: justify; padding-left: 90px;">Kontrol yang diperlukan dalam area ini adalah bentuk pengamanan terkait keberadaan aset informasi, termasuk keseluruhan proses yang bersifat teknis maupun administratif dalam siklus penggunaan aset tersebut.</p>
<p style="text-align: justify; padding-left: 90px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 60px;">►&nbsp; &nbsp;Teknologi dan Keamanan Informasi</p>
<p style="text-align: justify; padding-left: 90px;">Untuk kepentingan Indeks KAMI, aspek pengamanan di area teknologi mensyaratkan adanya strategi yang terkait dengan tingkatan risiko, dan tidak secara eksplisit menyebutkan teknologi atau merk pabrikan tertentu. Detail bentuk pengamanan yang dibahas di masing-masing area dapat dipahami dari pertanyaan (kajian mandiri) yang disediakan di area tersebut.</p>
<p style="text-align: justify; padding-left: 60px;">&nbsp;</p>
<p style="text-align: justify;">B.Tingkat Kematangan proses pengelolaan pengamanan informasi.</p>
<p style="text-align: justify; padding-left: 30px;">Metode yang kedua merupakan perluasan dari evaluasi kelengkapan dan digunakan untuk mengidentifikasi tingkat kematangan penerapan pengamanan dengan kategorisasi yang mengacu kepada tingkatan kematangan yang digunakan oleh kerangka kerja COBIT (Control Objective for Information and related Technology) atau CMMI (Capability Maturity Model for Integration).</p>
<p style="text-align: justify; padding-left: 30px;">Tingkat kematangan ini nantinya akan digunakan sebagai alat untuk melaporkan pemetaan dan pemeringkatan kesiapan keamanan informasi di Kementerian/Lembaga. Pemetaan dan pemeringkatan akan dilakukan Tim yang ditetapkan&nbsp;Kementerian Komunikasi dan Informatika (Kominfo) dan menjadi dasar bag pemberian OPINI Kominfo tentang kondisi tata kelola keamanan informasi di Kementerian/Lembaga terkait.</p>
<p style="text-align: justify; padding-left: 30px;">Tingkat kematangan ini nantinya akan digunakan sebagai alat untuk melaporkan pemetaan dan pemeringkatan kesiapan keamanan informasi di Kementerian/Lembaga. Pemetaan dan pemeringkatan akan dilakukan Tim yang ditetapkan</p>
<p style="text-align: justify; padding-left: 30px;">Kementerian Komunikasi dan Informatika (Kominfo) dan menjadi dasar bag pemberian OPINI Kominfo tentang kondisi tata kelola keamanan informasi di Kementerian/Lembaga terkait.</p>
<p style="text-align: justify; padding-left: 30px;">Untuk keperluan Indeks KAMI, tingkat kematangan tersebut didefinisikan sebagai berikut:</p>
<p style="text-align: justify; padding-left: 30px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat 0</strong>&ndash; Tidak Diketahui (PASIF)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Status kesiapan keamanan informasi tidak diketahui</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pihak yang terlibat tidak mengkuti atau tidak melaporkan pemeringkatan Indeks KAMI.</p>
<p style="text-align: justify; padding-left: 30px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat I-</strong>&nbsp;Kondisi Awal (REAKTIF)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Mulai adanya pemahaman mengenai perlunya pengelolaan keamanan informasi.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Penerapan langkah pengamanan masih bersifat reaktif, tidak teratur, tidak mengacu kepada keseluruhan risiko yang ada, tanpa alur komunikasi dan kewenangan yang jelas dan tanpa pengawasan.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Kelemahan teknis dan non-teknis tidak teridentifikasi dengan baik.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pihak yang terlibat tidak menyadari tanggung jawab mereka.</p>
<p style="text-align: justify; padding-left: 60px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat II</strong>&ndash; Penerapan Kerangka Kerja Dasar (AKTIF)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pengamanan sudah diterapkan walaupun sebagian besar masih di area teknis dan belum adanya keterkaitan langkah pengamanan untuk mendapatkan strategi yang efektif.</p>
<p style="text-align: justify; padding-left: 60px;">● Proses pengamanan berjalan tanpa dokumentasi atau rekaman resmi.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Langkah pengamanan operasional yang diterapkan bergantung kepada pengetahuan dan motivasi individu pelaksana.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Bentuk pengamanan secara keseluruhan belum dapat dibuktikan efektivitasnya.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Kelemahan dalam manajemen pengamanan masih banyak ditemukan dan tidak dapat diselesaikan dengan tuntas oleh pelaksana maupun pimpinan sehingga menyebabkan dampak yang sangat signifikan.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Manajemen pengamanan belum mendapatkan prioritas dan tidak berjalan secara konsisten.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pihak yang terlibat kemungkinan besar masih belum memahami tanggung jawab mereka.</p>
<p style="text-align: justify; padding-left: 60px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat III</strong>&ndash; Terdefinisi dan Konsisten (PRO AKTIF)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Bentuk pengamanan yang baku sudah diterapkan secara konsisten dan terdokumentasi secara resmi.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Efektivitas pengamanan dievaluasi secara berkala, walaupun belum melalui proses yang terstruktur.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pihak pelaksana dan pimpinan secara umum dapat menangani permasalahan terkait pengelolaan keamanan pengendalian dengan tepat, akan tetapi beberapa kelemahan dalam sistem manajemen masih ditemukan sehingga dapat mengakibatkan dampak yang signifikan.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Kerangka kerja pengamanan sudah mematuhi ambang batas minimum standar atau persyaratan hukum yang terkait.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Secara umum semua pihak yang terlibat menyadari tanggungjawab mereka dalam pengamanan informasi.</p>
<p style="text-align: justify; padding-left: 60px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat IV</strong>&ndash; Terkelola dan Terukur (TERKENDALI)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pengamanan diterapkan secara efektif sesuai dengan strategi manajemen risiko.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Evaluasi (pengukuran) pencapaian sasaran pengaman dilakukan secara rutin, formal dan terdokumentasi.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Penerapan pengamanan teknis secara konsisten dievaluasi</p>
<p style="text-align: justify; padding-left: 60px;">efektivitasnya.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Kelemahan manajemen pengamanan teridentifikasi dengan baik dan secara konsisten ditindaklanjuti pembenahannya.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Manajemen pengamanan bersifat pro-aktif dan menerapkan pembenahan untuk mencapai bentuk pengelolaan yang efisien.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Insiden dan ketidakpatuhan (non-conformity) diselesaikan melalui proses formal dengan pembelajaran akar permasalahan.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Karyawan merupakan bagian yang tidak terpisahkan dari pelaksana pengamanan informasi.</p>
<p style="text-align: justify; padding-left: 60px;">&nbsp;</p>
<p style="text-align: justify; padding-left: 30px;"><strong>Tingkat V</strong>&ndash; Optimal (OPTIMAL)</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pengamanan menyeluruh diterapkan secara berkelanjutan dan efektif melalui program pengelolaan risiko yang terstruktur.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Pengamanan informasi dan manajemen risiko sudah terintegrasi dengan tugas pokok instansi.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Kinerja pengamanan dievaluasi secara kontinyu, dengan analisisparameter efektivitas kontrol, kajian akar permasalahan dan penerapan langkah untuk optimasi peningkatan kinerja.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Target pencapaian program pengamanan informasi selalu dipantau, dievaluasi dan diperbaiki.</p>
<p style="text-align: justify; padding-left: 60px;">●&nbsp;Karyawan secara proaktif terlibat dalam peningkatan efektivita pengamanan.</p>
<p style="text-align: justify;">Catatan:</p>
<p style="text-align: justify;">Penyebutan peringkat dalam tanda kurung di atas yakni: PASIF, REAKTIF, AKTIF, PROAKTIF, TERKENDALI dan OPTIMAL merupakan OPINI yang diberikan Kominfo terhadap kondisi tata kelola keamanan informasi dari suatu Kementerian/Lembaga.</p>
